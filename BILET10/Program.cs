using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace exam
{
    internal class Program
    {
        static void Main(string[] args) // Главная функция, с которой начинается выполнение программы, принимает аргументы командной строки (не используются здесь).
        {
            Vehicle[] vehicles = new Vehicle[3]; // Создаем массив из 3 объектов типа Vehicle для хранения транспортных средств.

            vehicles[0] = new Plane(100, 200, 5000000, 900, 2020, 10000, 150); // Создаем объект Plane (самолет) и помещаем в ячейку 0: скорость 100, высота 200, цена 5 млн, пассажиров 900, год 2020, высота полета 10000, топливо 150.
            vehicles[1] = new Car(300, 400, 30000, 180, 2018); // Создаем объект Car (автомобиль) и помещаем в ячейку 1: скорость 300, вес 400, цена 30 тыс., мощность 180, год 2018.
            vehicles[2] = new Ship(500, 600, 1000000, 50, 2015, 2000, "Майами"); // Создаем объект Ship (корабль) и помещаем в ячейку 2: скорость 500, вес 600, цена 1 млн, пассажиров 50, год 2015, грузоподъемность 2000, порт "Майами".

            int i; // Переменная для счетчика цикла.
            for (i = 0; i < vehicles.Length; i++) // Цикл по всем элементам массива (0 до 2).
            {
                Console.WriteLine("Информация о транспортном средстве " + (i + 1) + ":"); // Выводим заголовок с номером транспортного средства (1, 2, 3).
                vehicles[i].ShowInfo(); // Вызываем метод ShowInfo для текущего транспортного средства для отображения его информации.
                Console.WriteLine(); // Добавляем пустую строку для разделения выводов.
            }
        }
    }
}